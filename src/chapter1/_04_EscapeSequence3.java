/*
 * More escape sequences
 * - get your program to print : 
 * 		This is line one
 * 		This is line two
 * 		This is line three
 */
package chapter1;

public class _04_EscapeSequence3 {

	public static void main(String[] args) {
		
		System.out.println("This is line one\nThis is line two\nThis is line three"); 
		
		// use \n to make 'n'ew lines! (\n= make new line)
		System.out.println("1\n2\n3\n5\n6\n8\n9\n100"); 
		
	}
	
}

