/* 
 * Know how to use escape sequences
 * - get your program to print: I said "Muwahahaha!" 
 */
package chapter1;

public class _02_EscapeSequence 
{

	public static void main(String[] args) 
	{
		 // escape sequence: starts with a backslash(\)  
		 // "를 프린트하기 위해선  \"를 같이 써야함  
		System.out.println("I said \"Muwahahaha!\" "); 
		System.out.println("I said \"김강진 짱짱맨!\"");
	}

}











