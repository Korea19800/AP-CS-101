package chapter7;

public class _02_WhileLoop {

	public static void main(String[] args) {

		// a simple for loop...
		for (int i = 0; i<= 10 ; i++ ) {
			System.out.print(i+" ");
		}

		// translated into a while loop


		
		

		// while loop execution order:
		// 0. init BEFORE the loop
		// 1. test
		// 2-1. if true execute body
		// 2-2. if false exit
			
	}
	
}
